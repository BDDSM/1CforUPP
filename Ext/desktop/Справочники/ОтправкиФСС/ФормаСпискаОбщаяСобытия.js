Ext.define('Справочники.ОтправкиФСС.ФормаСпискаОбщаяСобытия',
{
	ПередатьСсылку: function (строкаЗначений)
	{
		var ссылка = строкаЗначений.Ссылка;
		var хранилище = Ext.create('Ext.data.Store',
		{
			autoLoad: true,
			fields: ['Код','Наименование','ОтчетСсылка','ИдентификаторОтправкиНаСервере','ЗашифрованныйПакет','ИмяФайлаПакета','СтатусОтправки','СтатусОтправки.Ссылка','СтатусОтправки.Представление','Протокол','ДатаОтправки','ДатаПолученияРезультата','Организация','Организация.Ссылка','Организация.Представление','КодОрганаФСС','Квитанция',],
			proxy: new Ext.data.ScriptTagProxy({ url: 'https://as-msk-n7077:1337/Справочники/ОтправкиФСС/НайтиПоСсылке/' + ссылка, timeout:200}),
		});
		хранилище.load(
		{
			callback: function (records, operation, success)
			{
				Ext.require(['Справочники.ОтправкиФСС.ФормаЭлемента'], function ()
				{
					var количество = хранилище.getCount();
					if (количество == 0)
					{
						var массивЗначений = строкаЗначений;
					}
					else
					{
						var массивЗначений = хранилище.data.items[0].data;
					};
					var окно = Ext.create('Справочники.ОтправкиФСС.ФормаЭлемента',
					{
						Хранилище: массивЗначений.Родитель,
					});
					var форма = окно.down('form');
					форма.getForm().setValues(массивЗначений);
					if (окно)
					{
						окно.show();
						return окно;
					}
				});
			},
			scope: this
		});
	}
});