		public static ДокументыСсылка.РегламентированныйОтчет НайтиПоСсылке(Guid _Ссылка)
		{
			using (var Подключение = new SqlConnection(СтрокаСоединения))
			{
				Подключение.Open();
				using (var Команда = Подключение.CreateCommand())
				{
					Команда.CommandText = @"Select top 1 
					_IDRRef [Ссылка]
					,_Version [Версия]
					,_Marked [ПометкаУдаления]
					,_Fld11253 [ИсточникОтчета]
					,_Fld11254 [НаименованиеОтчета]
					,_Fld11255RRef [Организация]
					,_Fld11256 [ДатаНачала]
					,_Fld11257 [ДатаОкончания]
					,_Fld11258 [ДатаПодписи]
					,_Fld11259RRef [Периодичность]
					,_Fld11260RRef [ЕдиницаИзмерения]
					,_Fld11261 [ТочностьЕдиницыИзмерения]
					,_Fld11262 [ВыбраннаяФорма]
					,_Fld11263 [ДанныеОтчета]
					,_Fld11264 [ДеревоНастройкиСтраниц]
					,_Fld11265RRef [ВидОтчетности]
					,_Fld11266 [Комментарий]
					,_Fld11267 [КодИМНС]
					,_Fld22670 [КодИМНСПосредника]
					,_Fld11268 [ФорматВыгрузки]
					,_Fld11269 [Вид]
					,_Fld27288 [Период]
					,_Fld27289 [КПП]
					From _Document441(NOLOCK)
					Where _IDRRef=@Ссылка";
					Команда.Parameters.AddWithValue("Ссылка", _Ссылка);
					using (var Читалка = Команда.ExecuteReader())
					{
						if (Читалка.Read())
						{
							var Ссылка = new ДокументыСсылка.РегламентированныйОтчет();
							//ToDo: Читать нужно через GetValues()
							Ссылка.Ссылка = new Guid((byte[])Читалка.GetValue(0));
							var ПотокВерсии = ((byte[])Читалка.GetValue(1));
							Array.Reverse(ПотокВерсии);
							Ссылка.Версия =  BitConverter.ToInt64(ПотокВерсии, 0);
							Ссылка.ВерсияДанных =  Convert.ToBase64String(ПотокВерсии);
							Ссылка.ПометкаУдаления = ((byte[])Читалка.GetValue(2))[0]==1;
							Ссылка.ИсточникОтчета = Читалка.GetString(3);
							Ссылка.НаименованиеОтчета = Читалка.GetString(4);
							Ссылка.ДатаНачала = Читалка.GetDateTime(6);
							Ссылка.ДатаОкончания = Читалка.GetDateTime(7);
							Ссылка.ДатаПодписи = Читалка.GetDateTime(8);
							Ссылка.Периодичность = V82.Перечисления/*Ссылка*/.Периодичность.ПустаяСсылка.Получить((byte[])Читалка.GetValue(9));
							Ссылка.ЕдиницаИзмерения = V82.Перечисления/*Ссылка*/.ПорядкиОкругленияОтчетности.ПустаяСсылка.Получить((byte[])Читалка.GetValue(10));
							Ссылка.ТочностьЕдиницыИзмерения = Читалка.GetDecimal(11);
							Ссылка.ВыбраннаяФорма = Читалка.GetString(12);
							Ссылка.ВидОтчетности = V82.Перечисления/*Ссылка*/.ВидыОтчетности.ПустаяСсылка.Получить((byte[])Читалка.GetValue(15));
							Ссылка.Комментарий = Читалка.GetString(16);
							Ссылка.КодИМНС = Читалка.GetString(17);
							Ссылка.КодИМНСПосредника = Читалка.GetString(18);
							Ссылка.ФорматВыгрузки = Читалка.GetString(19);
							Ссылка.Вид = Читалка.GetDecimal(20);
							Ссылка.Период = Читалка.GetString(21);
							Ссылка.КПП = Читалка.GetString(22);
							return Ссылка;
						}
						else
						{
							return null;
						}
					}
				}
			}
		}
		
