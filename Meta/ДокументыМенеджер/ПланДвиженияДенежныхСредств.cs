		public static ДокументыСсылка.ПланДвиженияДенежныхСредств НайтиПоСсылке(Guid _Ссылка)
		{
			using (var Подключение = new SqlConnection(СтрокаСоединения))
			{
				Подключение.Open();
				using (var Команда = Подключение.CreateCommand())
				{
					Команда.CommandText = @"Select top 1 
					_IDRRef [Ссылка]
					,_Version [Версия]
					,_Marked [ПометкаУдаления]
					,_Fld12721RRef [Сценарий]
					,_Fld12722RRef [ВалютаДокумента]
					,_Fld12723 [ДатаПланирования]
					,_Fld12724 [Комментарий]
					,_Fld12725 [КратностьДокумента]
					,_Fld12726 [КурсДокумента]
					,_Fld12727RRef [Ответственный]
					,_Fld12728RRef [Подразделение]
					,_Fld12729RRef [ВидДенежныхСредств]
					From _Document506(NOLOCK)
					Where _IDRRef=@Ссылка";
					Команда.Parameters.AddWithValue("Ссылка", _Ссылка);
					using (var Читалка = Команда.ExecuteReader())
					{
						if (Читалка.Read())
						{
							var Ссылка = new ДокументыСсылка.ПланДвиженияДенежныхСредств();
							//ToDo: Читать нужно через GetValues()
							Ссылка.Ссылка = new Guid((byte[])Читалка.GetValue(0));
							var ПотокВерсии = ((byte[])Читалка.GetValue(1));
							Array.Reverse(ПотокВерсии);
							Ссылка.Версия =  BitConverter.ToInt64(ПотокВерсии, 0);
							Ссылка.ВерсияДанных =  Convert.ToBase64String(ПотокВерсии);
							Ссылка.ПометкаУдаления = ((byte[])Читалка.GetValue(2))[0]==1;
							Ссылка.ДатаПланирования = Читалка.GetDateTime(5);
							Ссылка.Комментарий = Читалка.GetString(6);
							Ссылка.КратностьДокумента = Читалка.GetDecimal(7);
							Ссылка.КурсДокумента = Читалка.GetDecimal(8);
							Ссылка.ВидДенежныхСредств = V82.Перечисления/*Ссылка*/.ВидыДенежныхСредств.ПустаяСсылка.Получить((byte[])Читалка.GetValue(11));
							return Ссылка;
						}
						else
						{
							return null;
						}
					}
				}
			}
		}
		
