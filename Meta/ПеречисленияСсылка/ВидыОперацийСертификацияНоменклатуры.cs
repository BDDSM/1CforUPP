		{
			switch (Значение)
			{
				case ВидыОперацийСертификацияНоменклатуры.Внутренняя: return Внутренняя;
				case ВидыОперацийСертификацияНоменклатуры.Внешняя: return Внешняя;
			}
			return Guid.Empty;
		}
	}
