		{
			if(Ссылка == Внутренняя)
			{
				return ВидыОперацийСертификацияНоменклатуры.Внутренняя;
			}
			else if(Ссылка == Внешняя)
			{
				return ВидыОперацийСертификацияНоменклатуры.Внешняя;
			}
			return ВидыОперацийСертификацияНоменклатуры.ПустаяСсылка;
		}
