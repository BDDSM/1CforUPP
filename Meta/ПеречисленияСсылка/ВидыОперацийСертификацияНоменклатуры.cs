	public static partial class ВидыОперацийСертификацияНоменклатуры_Значения//:ПеречислениеСсылка
	{
		public static readonly Guid Внутренняя = new Guid("1c010dad-2a73-1de7-4236-833c5ceb7378");
		public static readonly Guid Внешняя = new Guid("144a96a3-2a0c-612f-43b1-fd1a82513fe7");
