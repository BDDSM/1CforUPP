		public static СправочникиСсылка.ДокументыРеализацииПолномочийНалоговыхОрганов НайтиПоКоду(string Код)
		{
			using (var Подключение = new SqlConnection(СтрокаСоединения))
			{
				Подключение.Open();
				using (var Команда = Подключение.CreateCommand())
				{
					Команда.CommandText = @"Select top 1 
					_IDRRef [Ссылка]
					,_Version [Версия]
					,_Marked [ПометкаУдаления]
					,_IsMetadata [Предопределенный]
					,_Code [Код]
					,_Description [Наименование]
					,_Fld2249RRef [НалоговыйОрган]
					,_Fld2250RRef [Организация]
					,_Fld2251 [Идентификатор]
					,_Fld2252 [ИдентификаторОснования]
					,_Fld2253_TYPE [ВидДокумента_Тип],_Fld2253_RRRef [ВидДокумента],_Fld2253_RTRef [ВидДокумента_Вид]
					,_Fld2254 [НомерДокумента]
					,_Fld2255 [ДатаДокумента]
					,_Fld2256 [ДатаСообщения]
					,_Fld2257 [ДатаОтправки]
					From _Reference89(NOLOCK)
					Where _Code=@Код";
					Команда.Parameters.AddWithValue("Код", Код);
					using (var Читалка = Команда.ExecuteReader())
					{
						if (Читалка.Read())
						{
							var Ссылка = new СправочникиСсылка.ДокументыРеализацииПолномочийНалоговыхОрганов();
							//ToDo: Читать нужно через GetValues()
							Ссылка.Ссылка = new Guid((byte[])Читалка.GetValue(0));
							var ПотокВерсии = ((byte[])Читалка.GetValue(1));
							Array.Reverse(ПотокВерсии);
							Ссылка.Версия =  BitConverter.ToInt64(ПотокВерсии, 0);
							Ссылка.ПометкаУдаления = ((byte[])Читалка.GetValue(2))[0]==1?true:false;
							Ссылка.Предопределенный = ((byte[])Читалка.GetValue(3))[0]==1?true:false;
							Ссылка.Код = Читалка.GetString(4);
							Ссылка.Наименование = Читалка.GetString(5);
							//Ссылка.НалоговыйОрган = new V82.СправочникиСсылка.НалоговыеОрганы((byte[])Читалка.GetValue(6));
							//Ссылка.Организация = new V82.СправочникиСсылка.Организации((byte[])Читалка.GetValue(7));
							Ссылка.Идентификатор = Читалка.GetString(8);
							Ссылка.ИдентификаторОснования = Читалка.GetString(9);
							Ссылка.НомерДокумента = Читалка.GetString(13);
							Ссылка.ДатаДокумента = Читалка.GetDateTime(14);
							Ссылка.ДатаСообщения = Читалка.GetDateTime(15);
							Ссылка.ДатаОтправки = Читалка.GetDateTime(16);
							return Ссылка;
						}
						else
						{
							return null;
						}
					}
				}
			}
		}

