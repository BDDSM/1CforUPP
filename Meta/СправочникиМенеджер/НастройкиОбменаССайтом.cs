		public static V82.СправочникиОбъект.НастройкиОбменаССайтом СоздатьЭлемент()
		{
			var Объект = new V82.СправочникиОбъект.НастройкиОбменаССайтом();
			Объект._ЭтоНовый = true;
			Объект.Ссылка = Guid.NewGuid();/*http://msdn.microsoft.com/ru-ru/library/aa379322(VS.85).aspx*/
			Объект.ЭтоГруппа = false;
			Объект.КаталогВыгрузки = "";
			Объект.HTTPОбменАдресСайта = "";
			Объект.HTTPОбменИмяПользователя = "";
			Объект.HTTPОбменПароль = "";
			Объект.HTTPОбменПроксиСервер = "";
			Объект.HTTPОбменПроксиИмяПользователя = "";
			Объект.HTTPОбменПроксиПароль = "";
			Объект.СпособИдентификацииКонтрагентов = "";
			Объект.Комментарий = "";
			Объект.РегламентноеЗадание = "";
			Объект.ГруппаДляНовыхКонтрагентов = new V82.СправочникиСсылка.Контрагенты();
			Объект.ГруппаДляНовойНоменклатуры = new V82.СправочникиСсылка.Номенклатура();
			Объект.Организация = new V82.СправочникиСсылка.Организации();
			Объект.Ответственный = new V82.СправочникиСсылка.Пользователи();
			Объект.Подразделение = new V82.СправочникиСсылка.Подразделения();
			Объект.ЕдиницаИзмеренияНовойНоменклатуры = new V82.СправочникиСсылка.КлассификаторЕдиницИзмерения();
			return Объект;
		}
