							Ссылка.ИНН = Читалка.GetString(12);
							Ссылка.КПП = Читалка.GetString(13);
							Ссылка.ЮрФизЛицо = V82.Перечисления/*Ссылка*/.ЮрФизЛицо.ПустаяСсылка.Получить((byte[])Читалка.GetValue(14));
							//Ссылка.ОсновнойБанковскийСчет = new V82.СправочникиСсылка.БанковскиеСчета((byte[])Читалка.GetValue(15));
							//Ссылка.ОсновнойДоговорКонтрагента = new V82.СправочникиСсылка.ДоговорыКонтрагентов((byte[])Читалка.GetValue(16));
