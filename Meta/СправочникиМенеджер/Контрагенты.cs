		public static V82.СправочникиОбъект.Контрагенты СоздатьЭлемент()
		{
			var Объект = new V82.СправочникиОбъект.Контрагенты();
			Объект._ЭтоНовый = true;
			Объект.Ссылка = Guid.NewGuid();/*http://msdn.microsoft.com/ru-ru/library/aa379322(VS.85).aspx*/
			Объект.ЭтоГруппа = false;
			Объект.НаименованиеПолное = "";
			Объект.Комментарий = "";
			Объект.ДополнительноеОписание = "";
			Объект.КодПоОКПО = "";
			Объект.ИНН = "";
			Объект.КПП = "";
			Объект.ДокументУдостоверяющийЛичность = "";
			Объект.РасписаниеРаботыСтрокой = "";
			Объект.ГоловнойКонтрагент = new V82.СправочникиСсылка.Контрагенты();
			Объект.ИсточникИнформацииПриОбращении = new V82.СправочникиСсылка.ИсточникиИнформацииПриОбращенииПокупателей();
			Объект.ЮрФизЛицо = V82.Перечисления/*Ссылка*/.ЮрФизЛицо.ПустаяСсылка;
			Объект.ОсновнойБанковскийСчет = new V82.СправочникиСсылка.БанковскиеСчета();
			Объект.ОсновнойДоговорКонтрагента = new V82.СправочникиСсылка.ДоговорыКонтрагентов();
			Объект.ОсновнойВидДеятельности = new V82.СправочникиСсылка.ВидыДеятельностиКонтрагентов();
			Объект.ОсновнойМенеджерПокупателя = new V82.СправочникиСсылка.Пользователи();
			Объект.ОсновноеКонтактноеЛицо = new V82.СправочникиСсылка.КонтактныеЛицаКонтрагентов();
			Объект.ОКОПФ = new V82.СправочникиСсылка.КлассификаторОКОПФ();
			Объект.Регион = new V82.СправочникиСсылка.Регионы();
			Объект.ГруппаДоступаКонтрагента = new V82.СправочникиСсылка.ГруппыДоступаКонтрагентов();
			return Объект;
		}
