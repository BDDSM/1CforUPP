							//Ссылка.ОсновнойМенеджерПокупателя = new V82.СправочникиСсылка.Пользователи((byte[])Читалка.GetValue(19));
							Ссылка.Покупатель = ((byte[])Читалка.GetValue(20))[0]==1?true:false;
							Ссылка.Поставщик = ((byte[])Читалка.GetValue(21))[0]==1?true:false;
							Ссылка.РасписаниеРаботыСтрокой = Читалка.GetString(22);
