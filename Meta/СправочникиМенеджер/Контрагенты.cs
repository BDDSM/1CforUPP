							//Ссылка.ОКОПФ = new V82.СправочникиСсылка.КлассификаторОКОПФ((byte[])Читалка.GetValue(26));
							//Ссылка.Регион = new V82.СправочникиСсылка.Регионы((byte[])Читалка.GetValue(27));
							//Ссылка.ГруппаДоступаКонтрагента = new V82.СправочникиСсылка.ГруппыДоступаКонтрагентов((byte[])Читалка.GetValue(28));
